/**
 * This contains all the enums for direction.
 * @author Daniel
 *
 */
public enum Direction {
	/**
	 * To move up
	 */
	UP,
	/**
	 * To move down
	 */
	DOWN,
	/**
	 * To move left
	 */
	LEFT,
	/**
	 * To move right
	 */
	RIGHT,
	/**
	 * To move up and left
	 */
	UP_LEFT,
	/**
	 * To move up and right
	 */
	UP_RIGHT,
	/**
	 * To move down left
	 */
	DOWN_LEFT,
	/**
	 * To move left right
	 */
	DOWN_RIGHT,
}
